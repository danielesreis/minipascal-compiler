    private void saveFileButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveFileButtonActionPerformed
        int i;
        FileWriter output = null;
        try{
            output = new FileWriter(filePath.getText());
            BufferedWriter writer = new BufferedWriter(output);
            
            String aux[] = inputText1.getText().split("\n");
            
            for (i = 0; i < aux.length; i++)
            {
                writer.append(aux[i].trim());
                writer.newLine();
            }
            
            writer.close();
        } catch (Exception e) {
             throw new RuntimeException(e);
        } finally {
            if (output != null) {
                try {
                    output.flush();
                    output.close();
                } catch (IOException e) {

                }
            }
        }
    }//GEN-LAST:event_saveFileButtonActionPerformed

    private void openFileButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openFileButtonActionPerformed
        // TODO add your handling code here:
        JFileChooser jFileChooser = new JFileChooser();
        
        jFileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        String code = "";
        int result = jFileChooser.showOpenDialog(this);
        
        if (result == JFileChooser.APPROVE_OPTION)
        {
            File selectedFile = jFileChooser.getSelectedFile();
            if (!selectedFile.getPath().toLowerCase().endsWith(".pas")) {
                JOptionPane.showMessageDialog(this, "A extensão do arquivo deve ser do tipo .pas!");
                return;
            }
            try {
                filePath.setText(selectedFile.getPath());
                code = readFile(selectedFile.getPath());
            } catch (IOException ex) {
                Logger.getLogger(CompilerFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        inputText1.setText(code.trim());
    }//GEN-LAST:event_openFileButtonActionPerformed

