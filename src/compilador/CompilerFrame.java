    private void saveAsFileButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveAsFileButtonActionPerformed
        // TODO add your handling code here:
        JFileChooser jFileChooser = new JFileChooser();
        int result = jFileChooser.showSaveDialog(null);
        
        if (result == JFileChooser.APPROVE_OPTION) {
            String code = inputText1.getText();
            BufferedWriter file;
            File path;
            try {
                path = jFileChooser.getSelectedFile();
                
                String filePath = path.getPath();
                if(!filePath.toLowerCase().endsWith(".pas")) path = new File(filePath + ".pas");
                    
                file = new BufferedWriter(new FileWriter(path));
                file.write(code);
                file.close();
            } catch (IOException ex) {
                Logger.getLogger(CompilerFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_saveAsFileButtonActionPerformed

    }
    
    private String readFile(String file) throws IOException {
    BufferedReader reader = new BufferedReader(new FileReader (file));
    String         line = null;
    StringBuilder  stringBuilder = new StringBuilder();

    try {
        while((line = reader.readLine()) != null) {
            stringBuilder.append(line.trim());
            stringBuilder.append("\n");
        }

        return stringBuilder.toString();
    } finally {
        reader.close();
    }
}
